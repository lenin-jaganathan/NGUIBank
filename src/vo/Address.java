package vo;

public class Address {

	public Address(String city, int houseNumber, long zip, String area, String state) {
		this.city = city;
		this.houseNumber = houseNumber;
		this.zip = zip;
		this.area = area;
		this.state = state;
	}
	public String city ;
	public int houseNumber ;
	public long zip ;
	public String area ;
	public String state ;
	
	
	
}
